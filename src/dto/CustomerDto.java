package dto;

public abstract class CustomerDto {

    private String name;
    private Integer age;

    public CustomerDto() {}

    public CustomerDto(String name, Integer age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return this.age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
}
